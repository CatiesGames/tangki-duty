function genColloquialQuestion(b) {
  const o = R(Q_PARTS.openers);
  const p = R(Q_PARTS.problems);
  const d = R(Q_PARTS.details);
  const a = R(Q_PARTS.asks);
  return `${b.name}：${o}${o ? '，' : ''}我${p}，${d}，${a}`;
}

export function genQuestion(b) {
  if (Math.random() < b.irreverent * 0.55) return R(Q_IRREVERENT)(b);
  return Math.random() > 0.4 ? R(Q_FULL)(b) : genColloquialQuestion(b);
}

