export function genReplies(god, believerTag) {
  const pool = GOD_REPLY_POOLS[god?.replyFlavor] ?? REPLY_CORES;
  const picks = PICK_N(pool, 3);
  const specialtyHit = god?.specialties?.includes(believerTag);

  return picks.map((c, i) => {
    let text = R(c.lines);
    if (specialtyHit && i === 0 && SPECIALTY_REPLIES[believerTag]) {
      text = SPECIALTY_REPLIES[believerTag];
    }
    return { tone: c.tone, text: text + R(REPLY_ASIDES) };
  });
}

