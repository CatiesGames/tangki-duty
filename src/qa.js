/* 取得某信眾在某 stage 的「題目」與 stance */
export function getStage(believer, stageIdx) {
  const story = STORIES[believer.cid];
  if (story && story[stageIdx]) {
    const s = story[stageIdx];
    const ask = s.ask.replace('{name}', believer.name);
    // topic：故事 stage 顯式 topic 優先，否則從題目文字推斷，再否則用信徒固定 tag
    const topic = s.topic || inferTopic(ask) || believer.tag;
    return { ask, stance: s.stance, topic, last: stageIdx >= story.length - 1, hasStory: true };
  }
  // 無專屬故事 → 單階段 generic
  const ask = GENERIC_ASKS[believer.tag] ?? GENERIC_ASKS.感情;
  return { ask, stance: STANCE_FALLBACK[believer.tag] ?? 'stop', topic: inferTopic(ask) || believer.tag, last: true, hasStory: false };
}

/* 信眾對玩家選擇的反應（推進劇情） */
export function reactionLine(believer, stageIdx, persona) {
  const story = STORIES[believer.cid];
  if (story && story[stageIdx] && story[stageIdx].react) return story[stageIdx].react[persona] ?? '';
  return '';
}

/* 產生三個神諭選項：bold / steady / (greedy|bull)，帶該神明口吻 */
export function genReplyOptions(god, believer, stance, topic) {
  const third = Math.random() < 0.5 ? 'greedy' : 'bull';
  const order = shuffle(['bold', 'steady', third]);
  // 切題用的主題：以「這一階段的題目主題」優先，沒有才退回信徒固定 tag
  const askTopic = topic || believer.tag;
  const specialtyHit = god?.specialties?.includes(askTopic);
  return order.map((persona) => {
    const base = effectsFor(persona, stance);
    const effects = { ...base.effects };
    const tags = [...base.tags];
    const isMatch = (
      (stance === 'soothe' && persona === 'steady')
      || (stance === 'stop' && persona === 'steady')
      || (stance === 'push' && persona === 'bold')
    );
    if (specialtyHit && isMatch) { effects.faith += 1; if (!tags.includes('honest')) tags.push('honest'); }
    return {
      persona,
      personaLabel: PERSONAS[persona].label,
      text: replyText(god, believer, persona, askTopic),
      effects,
      tags,
      keepCombo: !!base.keepCombo, // 唬爛：不中斷 combo（也不推進）
    };
  });
}

function shuffle(a) {
  const arr = [...a];
  for (let i = arr.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [arr[i], arr[j]] = [arr[j], arr[i]]; }
  return arr;
}

/* ── 結算：人格鑑定 + 成就 + 反諷 ── */
export function judgePersona(score) {
  const { persona, honest, answers } = score;
  const top = Object.entries(persona).sort((a, b) => b[1] - a[1])[0]?.[0] ?? 'steady';
  const TITLES = {
    greedy: { title: '香油錢之王', desc: '信眾的錢包，比他們的問題更早被你解決。' },
    bull: { title: '金句製造機', desc: '說了很多，內容為零。但聽起來真的很玄。' },
    bold: { title: '頭過身就過', desc: '你叫每個人都去衝，反正後果他們自己扛。' },
    steady: honest > answers * 0.5
      ? { title: '佛系乩身（不適合本行）', desc: '你真心幫了人——這在本行是致命傷。' }
      : { title: '老神在在', desc: '穩，但廟方覺得你不夠積極招財。' },
  };
  return TITLES[top] ?? TITLES.steady;
}

