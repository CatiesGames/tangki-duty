function inferTopic(ask) {
  const s = String(ask || '');
  for (const [topic, kws] of TOPIC_KEYWORDS) {
    if (kws.some((k) => s.includes(k))) return topic;
  }
  return null;
}

/* 主題感知台詞：依 (topic, persona) 取一句真的在回答問題的話；缺則回 null */
function topicLine(topic, persona) {
  const pool = TOPIC_VOICE[topic];
  if (!pool) return null;
  const lines = pool[persona];
  if (lines && lines.length) return R(lines);
  // greedy/bull 若該主題沒寫，退到 steady 語感（仍切題）
  const fallback = pool.steady;
  return fallback && fallback.length ? R(fallback) : null;
}

/* 決定這個選項要用切題台詞還是通用神明台詞。
   有對應主題池時「以切題為主」，只偶爾摻神明口吻，確保回答真的對得上問題。 */
function replyText(god, believer, persona, topic) {
  const t = topicLine(topic ?? believer?.tag, persona);
  if (t) {
    // 有切題句：85% 用切題（回答對得上題目），15% 用神明口吻增添個性
    return Math.random() < 0.85 ? t : godLine(god.id, persona);
  }
  return godLine(god.id, persona);
}

/* persona × stance → 效果與隱藏標記。每種回答都是「有取捨」的真選擇，沒有純劣勢選項：
   - steady 對症：收入中上＋滿意↑↑＋衝 combo（永續流派，靠 combo＋名聲滾雪球；但最耗心力/元神）
   - bold：高收入、對症滿意↑、不對症當下爽但 harm（賭一把）
   - greedy：最高收入但滿意↓（坑錢流派，短多長空）
   - bull（唬爛）：省力安全牌——元神「回復」、滿意不掉、**不中斷 combo**，收入中等；
     代價是它不「推進」combo、不漲名聲（原地踏步），且暗藏 bull 計分（結算反諷）。
     → 元神快見底、或想保住連段喘口氣時的戰術選擇，而非純坑自己。 */
function effectsFor(persona, stance) {
  const goodMatch = (
    (stance === 'soothe' && persona === 'steady')
    || (stance === 'stop' && persona === 'steady')
    || (stance === 'push' && persona === 'bold')
  );
  if (persona === 'greedy') return { effects: { incense: 4, faith: -2, stable: 0 }, tags: stance === 'spend' ? ['scam'] : ['scam', 'harm'] };
  // 唬爛：省力安全牌——回元神、滿意持平、收入中等，且不斷 combo（見 main.js）
  if (persona === 'bull') return { effects: { incense: 2, faith: 0, stable: 2 }, tags: ['bull'], keepCombo: true };
  if (persona === 'bold') {
    if (goodMatch) return { effects: { incense: 2, faith: 2, stable: 0 }, tags: ['honest'] };
    if (stance === 'stop') return { effects: { incense: 3, faith: 1, stable: -1 }, tags: ['harm'] };
    return { effects: { incense: 2, faith: 1, stable: 0 }, tags: [] };
  }
  // steady（好好答）：對症收入更好、滿意更高；不對症仍有基本收入
  if (goodMatch) return { effects: { incense: 2, faith: 3, stable: 1 }, tags: ['honest'] };
  return { effects: { incense: 1, faith: 1, stable: 1 }, tags: [] };
}

